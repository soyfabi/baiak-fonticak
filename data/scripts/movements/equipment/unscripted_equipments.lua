	local movement = MoveEvent()
	movement:id(i.itemid or i.itemId)

	if(i.type) then
		movement:type(i.type)
	end
	if(i.slot) then
		movement:slot(i.slot)
	end
	if(i.level) then
		movement:level(i.level)
	end
	if(i.vocation) then
		for _, v in ipairs(i.vocation) do
			movement:vocation(v[1], v[2] or false, v[3] or false)
		end
	end
	
	if i.remove then
        movement:onStepIn(function(player, item, position, fromPosition)
            if item:getId() == i.itemid then
                item:remove()
                return true
            end
            return false
        end)
    end
	movement:register()
end
